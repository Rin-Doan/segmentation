# Configuration
SEGMENTED_DATA_PATH = '../../../../../vast/s222440401/segmented_data'
TRAINING_IMAGES_PATH = '../../../../../vast/s222440401/training_images'
NUM_CLASSES = 9  # Background + C1-C7 + other vertebrae
TARGET_SPACING = (2.0, 1.0, 1.0)  # (z, y, x) spacing in mm (same as generate_segmented_data.py)
TARGET_SHAPE = (64, 256, 256)  # (D, H, W) output shape (same as generate_segmented_data.py)

# Class names and colors
CLASS_NAMES = ['Background', 'C1', 'C2', 'C3', 'C4', 'C5', 'C6', 'C7', 'Other']
