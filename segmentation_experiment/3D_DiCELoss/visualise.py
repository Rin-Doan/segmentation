def load_segmentation_nifti(nifti_path):
    """
    Load segmentation NIfTI file and apply coordinate correction
    
    Args:
        nifti_path: Path to NIfTI file
    
    Returns:
        seg_volume: 3D numpy array (D, H, W) = (z, y, x)
        spacing: (z, y, x) spacing in mm
    """
    nii = nib.load(nifti_path)
    seg_volume = nii.get_fdata()
    
    # Apply coordinate correction (reverse of what was done in save_segmentation_as_nifti)
    # NIfTI is saved as (x, y[::-1], z[::-1])
    # To get back to (z, y, x): reverse y and z, then transpose
    seg_volume = seg_volume[:, ::-1, ::-1]  # Reverse y and z
    seg_volume = seg_volume.transpose(2, 1, 0)  # (x, y, z) -> (z, y, x)
    
    # Get spacing (NIfTI spacing is (x, y, z), convert to (z, y, x))
    nifti_spacing = nii.header.get_zooms()[:3]  # (x, y, z)
    spacing = (nifti_spacing[2], nifti_spacing[1], nifti_spacing[0])  # (z, y, x)
    
    return seg_volume.astype(np.int16), spacing


def load_dicom_volume(study_dir, preprocess=True):
    """
    Load entire DICOM series as 3D volume, optionally preprocess to match segmentation
    
    Args:
        study_dir: Directory containing DICOM files
        preprocess: If True, preprocess to match segmentation shape and spacing
    
    Returns:
        volume: 3D numpy array (D, H, W)
        spacing: (z, y, x) spacing in mm (if preprocess=False, returns None)
    """
    # Find all DICOM files
    dicom_files = []
    for root, _, files in os.walk(study_dir):
        for file in files:
            if file.lower().endswith('.dcm'):
                dicom_files.append(os.path.join(root, file))
    
    if not dicom_files:
        return None, None
    
    # Sort by InstanceNumber
    try:
        dicom_meta = []
        for dcm_path in dicom_files:
            ds = pydicom.dcmread(dcm_path, stop_before_pixels=True, force=True)
            instance_num = getattr(ds, 'InstanceNumber', 0)
            dicom_meta.append((instance_num, dcm_path))
        dicom_meta.sort(key=lambda x: x[0])
        sorted_dicom_paths = [path for _, path in dicom_meta]
    except Exception as e:
        sorted_dicom_paths = sorted(dicom_files)
    
    # Load all slices
    slices = []
    for dcm_path in sorted_dicom_paths:
        try:
            ds = pydicom.dcmread(dcm_path)
            slice_data = ds.pixel_array.astype(np.float32)
            slices.append(slice_data)
        except Exception as e:
            continue
    
    if not slices:
        return None, None
    
    # Stack into 3D volume (D, H, W)
    volume = np.stack(slices, axis=0)
    
    # Preprocess to match segmentation if requested
    if preprocess and PREPROCESSING_AVAILABLE:
        # Get spacing
        dicom_spacing = get_spacing_from_dicom_series(sorted_dicom_paths)
        
        # HU windowing and normalization (same as in generate_segmented_data.py)
        volume = np.clip(volume, -200, 1800)
        volume = (volume - (-200)) / (1800 - (-200))  # Scale to [0, 1]
        
        # Resample to standard physical spacing
        volume = resample_to_standard_spacing_3d(
            volume, dicom_spacing, TARGET_SPACING, order=1
        )
        
        # Crop or pad to target shape
        volume = crop_or_pad_to_size(volume, TARGET_SHAPE)
        
        return volume, TARGET_SPACING
    
    return volume, None


def create_colormap():
    """Create custom colormap for segmentation classes"""
    # Create colormap with custom colors
    colors = ['#000000']  # Background (black)
    colors.extend(CLASS_COLORS[1:])  # Add other class colors
    
    # Pad to 20 colors for tab20 compatibility
    while len(colors) < 20:
        colors.append('#808080')  # Gray for padding
    
    return ListedColormap(colors[:NUM_CLASSES])


def visualize_3d_segmentation(seg_volume, image_volume=None, study_id=None, 
                              save_path=None, show_overlay=True):
    """
    Visualize 3D segmentation in three orthogonal views
    
    Args:
        seg_volume: 3D numpy array (D, H, W) with class labels
        image_volume: Optional 3D numpy array (D, H, W) with original image
        study_id: Study ID for title
        save_path: Path to save visualization (if None, displays interactively)
        show_overlay: Whether to overlay segmentation on original image
    """
    D, H, W = seg_volume.shape
    
    # Get middle slices for each view
    mid_axial = D // 2
    mid_coronal = H // 2
    mid_sagittal = W // 2
    
    # Create figure with subplots
    if show_overlay and image_volume is not None:
        fig, axes = plt.subplots(3, 3, figsize=(18, 18))
        fig.suptitle(f'Segmentation Visualization: {study_id or "Unknown"}', 
                     fontsize=16, fontweight='bold')
    else:
        fig, axes = plt.subplots(1, 3, figsize=(18, 6))
        if study_id:
            fig.suptitle(f'Segmentation Visualization: {study_id}', 
                        fontsize=16, fontweight='bold')
    
    # Create custom colormap
    cmap = create_colormap()
    
    if show_overlay and image_volume is not None:
        # Normalize image for display
        img_min, img_max = np.percentile(image_volume, [1, 99])
        image_volume_norm = np.clip((image_volume - img_min) / (img_max - img_min), 0, 1)
        
        # Axial view (top row)
        axes[0, 0].imshow(image_volume_norm[mid_axial], cmap='gray')
        axes[0, 0].set_title('Axial - Original Image', fontsize=12, fontweight='bold')
        axes[0, 0].axis('off')
        
        axes[0, 1].imshow(seg_volume[mid_axial], cmap=cmap, vmin=0, vmax=NUM_CLASSES-1, alpha=0.8)
        axes[0, 1].set_title('Axial - Segmentation', fontsize=12, fontweight='bold')
        axes[0, 1].axis('off')
        
        # Overlay
        axes[0, 2].imshow(image_volume_norm[mid_axial], cmap='gray')
        seg_mask = seg_volume[mid_axial] > 0
        axes[0, 2].imshow(seg_volume[mid_axial], cmap=cmap, vmin=0, vmax=NUM_CLASSES-1, 
                         alpha=0.5, interpolation='nearest')
        axes[0, 2].set_title('Axial - Overlay', fontsize=12, fontweight='bold')
        axes[0, 2].axis('off')
        
        # Coronal view (middle row)
        axes[1, 0].imshow(image_volume_norm[:, mid_coronal, :], cmap='gray')
        axes[1, 0].set_title('Coronal - Original Image', fontsize=12, fontweight='bold')
        axes[1, 0].axis('off')
        
        axes[1, 1].imshow(seg_volume[:, mid_coronal, :], cmap=cmap, vmin=0, vmax=NUM_CLASSES-1, alpha=0.8)
        axes[1, 1].set_title('Coronal - Segmentation', fontsize=12, fontweight='bold')
        axes[1, 1].axis('off')
        
        # Overlay
        axes[1, 2].imshow(image_volume_norm[:, mid_coronal, :], cmap='gray')
        axes[1, 2].imshow(seg_volume[:, mid_coronal, :], cmap=cmap, vmin=0, vmax=NUM_CLASSES-1, 
                         alpha=0.5, interpolation='nearest')
        axes[1, 2].set_title('Coronal - Overlay', fontsize=12, fontweight='bold')
        axes[1, 2].axis('off')
        
        # Sagittal view (bottom row)
        axes[2, 0].imshow(image_volume_norm[:, :, mid_sagittal], cmap='gray')
        axes[2, 0].set_title('Sagittal - Original Image', fontsize=12, fontweight='bold')
        axes[2, 0].axis('off')
        
        axes[2, 1].imshow(seg_volume[:, :, mid_sagittal], cmap=cmap, vmin=0, vmax=NUM_CLASSES-1, alpha=0.8)
        axes[2, 1].set_title('Sagittal - Segmentation', fontsize=12, fontweight='bold')
        axes[2, 1].axis('off')
        
        # Overlay
        axes[2, 2].imshow(image_volume_norm[:, :, mid_sagittal], cmap='gray')
        axes[2, 2].imshow(seg_volume[:, :, mid_sagittal], cmap=cmap, vmin=0, vmax=NUM_CLASSES-1, 
                         alpha=0.5, interpolation='nearest')
        axes[2, 2].set_title('Sagittal - Overlay', fontsize=12, fontweight='bold')
        axes[2, 2].axis('off')
    else:
        # Just segmentation without overlay
        axes[0].imshow(seg_volume[mid_axial], cmap=cmap, vmin=0, vmax=NUM_CLASSES-1)
        axes[0].set_title('Axial View', fontsize=12, fontweight='bold')
        axes[0].axis('off')
        
        axes[1].imshow(seg_volume[:, mid_coronal, :], cmap=cmap, vmin=0, vmax=NUM_CLASSES-1)
        axes[1].set_title('Coronal View', fontsize=12, fontweight='bold')
        axes[1].axis('off')
        
        axes[2].imshow(seg_volume[:, :, mid_sagittal], cmap=cmap, vmin=0, vmax=NUM_CLASSES-1)
        axes[2].set_title('Sagittal View', fontsize=12, fontweight='bold')
        axes[2].axis('off')
    
    # Add legend
    from matplotlib.patches import Patch
    legend_elements = [Patch(facecolor=CLASS_COLORS[i], label=CLASS_NAMES[i]) 
                      for i in range(NUM_CLASSES)]
    fig.legend(handles=legend_elements, loc='lower center', ncol=5, fontsize=10)
    
    plt.tight_layout(rect=[0, 0.05, 1, 0.98])
    
    if save_path:
        plt.savefig(save_path, dpi=200, bbox_inches='tight')
        print(f"✓ Visualization saved to '{save_path}'")
        plt.close()
    else:
        # Check if we have a display, otherwise save to default location
        if 'DISPLAY' not in os.environ:
            # No display available, save to current directory
            default_save_path = f"{study_id or 'visualization'}_output.png"
            plt.savefig(default_save_path, dpi=200, bbox_inches='tight')
            print(f"✓ No display available. Visualization saved to '{default_save_path}'")
            plt.close()
        else:
            plt.show()


def visualize_study(study_id, show_overlay=True, save_dir=None):
    """
    Visualize segmentation for a specific study
    
    Args:
        study_id: Study ID (directory name)
        show_overlay: Whether to overlay on original DICOM images
        save_dir: Directory to save visualization (if None, displays interactively)
    """
    # Load segmentation
    seg_path = os.path.join(SEGMENTED_DATA_PATH, f"{study_id}.nii.gz")
    if not os.path.exists(seg_path):
        print(f"Error: Segmentation file not found: {seg_path}")
        return
    
    print(f"Loading segmentation: {seg_path}")
    seg_volume, spacing = load_segmentation_nifti(seg_path)
    print(f"  Shape: {seg_volume.shape}, Spacing: {spacing}")
    
    # Load original image if overlay requested
    image_volume = None
    if show_overlay:
        study_dir = os.path.join(TRAINING_IMAGES_PATH, study_id)
        if os.path.exists(study_dir):
            print(f"Loading original DICOM images: {study_dir}")
            image_volume, _ = load_dicom_volume(study_dir, preprocess=True)
            if image_volume is not None:
                print(f"  Shape: {image_volume.shape}")
                # Check if shapes match
                if image_volume.shape != seg_volume.shape:
                    print(f"  Warning: Image shape {image_volume.shape} != Segmentation shape {seg_volume.shape}")
                    print(f"  Shapes don't match - overlay may not work correctly")
        else:
            print(f"  Warning: DICOM directory not found: {study_dir}")
            print(f"  Showing segmentation only")
    
    # Determine save path
    save_path = None
    if save_dir:
        os.makedirs(save_dir, exist_ok=True)
        save_path = os.path.join(save_dir, f"{study_id}_visualization.png")
    elif 'DISPLAY' not in os.environ:
        # No display available, save to current directory
        save_path = f"{study_id}_visualization.png"
    
    # Visualize
    print("Generating visualization...")
    visualize_3d_segmentation(seg_volume, image_volume, study_id, save_path, show_overlay)


def visualize_all_studies(show_overlay=True, save_dir=None, max_studies=None):
    """
    Visualize all studies in segmented_data directory
    
    Args:
        show_overlay: Whether to overlay on original DICOM images
        save_dir: Directory to save visualizations (if None, displays interactively)
        max_studies: Maximum number of studies to visualize (None for all)
    """
    if not os.path.exists(SEGMENTED_DATA_PATH):
        print(f"Error: Segmented data path does not exist: {SEGMENTED_DATA_PATH}")
        return
    
    # Find all NIfTI files
    nifti_files = [f for f in os.listdir(SEGMENTED_DATA_PATH) 
                   if f.endswith('.nii.gz') or f.endswith('.nii')]
    study_ids = [f.replace('.nii.gz', '').replace('.nii', '') for f in nifti_files]
    study_ids = sorted(study_ids)
    
    if max_studies:
        study_ids = study_ids[:max_studies]
    
    print(f"Found {len(study_ids)} studies to visualize")
    print("="*80)
    
    for study_id in tqdm(study_ids, desc="Visualizing studies"):
        visualize_study(study_id, show_overlay, save_dir)
    
    print("="*80)
    print("Visualization complete!")


def main():
    """Main function"""
    parser = argparse.ArgumentParser(description='Visualize segmented data')
    parser.add_argument('--study-id', type=str, default=None,
                       help='Specific study ID to visualize (if not provided, visualizes all)')
    parser.add_argument('--no-overlay', action='store_true',
                       help='Do not overlay segmentation on original images')
    parser.add_argument('--save-dir', type=str, default=None,
                       help='Directory to save visualizations (if not provided, displays interactively)')
    parser.add_argument('--max-studies', type=int, default=None,
                       help='Maximum number of studies to visualize (only when visualizing all)')
    
    args = parser.parse_args()
    
    print("="*80)
    print("3D SEGMENTATION VISUALIZATION")
    print("="*80)
    print()
    
    show_overlay = not args.no_overlay
    
    if args.study_id:
        # Visualize specific study
        visualize_study(args.study_id, show_overlay, args.save_dir)
    else:
        # Visualize all studies
        visualize_all_studies(show_overlay, args.save_dir, args.max_studies)


if __name__ == "__main__":
    main()

